package com.irad.dar.cctns;

public class Victim {
	public String victim_age;
    public String victim_name;
    public String victim_dob;
    public String victim_pres_address;
	public String getVictim_age() {
		return victim_age;
	}
	public void setVictim_age(String victim_age) {
		this.victim_age = victim_age;
	}
	public String getVictim_name() {
		return victim_name;
	}
	public void setVictim_name(String victim_name) {
		this.victim_name = victim_name;
	}
	public String getVictim_dob() {
		return victim_dob;
	}
	public void setVictim_dob(String victim_dob) {
		this.victim_dob = victim_dob;
	}
	public String getVictim_pres_address() {
		return victim_pres_address;
	}
	public void setVictim_pres_address(String victim_pres_address) {
		this.victim_pres_address = victim_pres_address;
	}
    
    
    
}
