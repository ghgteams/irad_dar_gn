package com.irad.dar.cctns;

public class AccusedList {
	public String accused_name;
    public String accused_pres_addr;
    public String accused_age;
    
    
	public String getAccused_name() {
		return accused_name;
	}
	public void setAccused_name(String accused_name) {
		this.accused_name = accused_name;
	}
	public String getAccused_pres_addr() {
		return accused_pres_addr;
	}
	public void setAccused_pres_addr(String accused_pres_addr) {
		this.accused_pres_addr = accused_pres_addr;
	}
	public String getAccused_age() {
		return accused_age;
	}
	public void setAccused_age(String accused_age) {
		this.accused_age = accused_age;
	}
    
    
}
