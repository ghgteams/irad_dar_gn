package com.irad.dar.pdf;

public class InjuredDetails {
	private String injurySeverity;
	private String nameOfInjured;
	private String ageOfInjured;
	private String occupationOfInjured;
	private String natureOfInjury;
	private String detailsOfInjury;
	public String getInjurySeverity() {
		return injurySeverity;
	}
	public void setInjurySeverity(String injurySeverity) {
		this.injurySeverity = injurySeverity;
	}
	public String getNameOfInjured() {
		return nameOfInjured;
	}
	public void setNameOfInjured(String nameOfInjured) {
		this.nameOfInjured = nameOfInjured;
	}
	public String getAgeOfInjured() {
		return ageOfInjured;
	}
	public void setAgeOfInjured(String ageOfInjured) {
		this.ageOfInjured = ageOfInjured;
	}
	public String getOccupationOfInjured() {
		return occupationOfInjured;
	}
	public void setOccupationOfInjured(String occupationOfInjured) {
		this.occupationOfInjured = occupationOfInjured;
	}
	public String getNatureOfInjury() {
		return natureOfInjury;
	}
	public void setNatureOfInjury(String natureOfInjury) {
		this.natureOfInjury = natureOfInjury;
	}
	public String getDetailsOfInjury() {
		return detailsOfInjury;
	}
	public void setDetailsOfInjury(String detailsOfInjury) {
		this.detailsOfInjury = detailsOfInjury;
	}
	
}
